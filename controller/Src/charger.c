/*--------------------------------- Includes ---------------------------------*/
#include "charger.h"
/*-------------------------------- Structures --------------------------------*/
/*--------------------------------- Constant ---------------------------------*/
/*-------------------------------- Variables ---------------------------------*/
static GPIO_TypeDef* chargerPort = { 0U };
static uint16_t      chargerPin  = 0U;
/*-------------------------------- Functions ---------------------------------*/

/*----------------------------------------------------------------------------*/
/*----------------------- PRIVATE --------------------------------------------*/
/*----------------------------------------------------------------------------*/

/*----------------------------------------------------------------------------*/
/*----------------------- PABLICK --------------------------------------------*/
/*----------------------------------------------------------------------------*/
void vCHARGERinit ( const GPIO_TYPE* gpio )
{
  chargerPort = gpio->port;
  chargerPin  = gpio->pin;
  return;
}

void vCHARGERset ( RELAY_STATUS status )
{
  if ( status == RELAY_OFF )
  {
    HAL_GPIO_WritePin( chargerPort, chargerPin, GPIO_PIN_RESET );
  }
  else
  {
    HAL_GPIO_WritePin( chargerPort, chargerPin, GPIO_PIN_SET );
  }
  return;
}
